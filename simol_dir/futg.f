      SUBROUTINE FUTG (F,U,G,N)
      IMPLICIT REAL*8 (A-H,O-Z)
      PARAMETER(ZERO=0.D0)
C     *
C     F = UT * G
       
      DIMENSION F(*),U(N,N),G(*)
C
      DO 10 I=1,N    
      F(I)   = ZERO
      DO 10 J=1,N    
 10   F(I)   = F(I) + U(J,I)*G(J)
      RETURN
      E N D
C
